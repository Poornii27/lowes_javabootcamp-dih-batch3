package com.lowes.assignments.java;

public class SwapNumbers {
    public static void main(String[] args) {
        int a = 5;
        int b = 7;
        int temp = a;


        a = b;
        b = temp;

        System.out.println("Value of a is" + a);
        System.out.println("Value of b is" + b);

    }
}

